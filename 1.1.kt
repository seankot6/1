fun main() {
    println("## Типы данных в Kotlin")
    println()

    println("### Основные типы:")
    println(" - Byte:  8-битный целочисленный тип (от -128 до 127)")
    println(" - Short: 16-битный целочисленный тип (от -32768 до 32767)")
    println(" - Int: 32-битный целочисленный тип")
    println(" - Long: 64-битный целочисленный тип")
    println(" - Float: 32-битный тип с плавающей точкой")
    println(" - Double: 64-битный тип с плавающей точкой")
    println(" - Char:  единичный символ")
    println(" - String:  последовательность символов")
    println(" - Boolean: логическое значение (true или false)")
    println()

    println("### Специальные типы:")
    println(" - Any:  базовый тип, от которого наследуются все остальные типы")
    println(" - Unit:  тип, представляющий отсутствие значения")
    println(" - Nothing: тип, который никогда не может быть создан")
    println(" - Array<T>: массив элементов одного типа")
    println(" - List<T>: неизменяемый список элементов одного типа")
    println(" - MutableList<T>: изменяемый список элементов одного типа")
    println(" - Set<T>:  неизменяемый набор уникальных элементов")
    println(" - MutableSet<T>: изменяемый набор уникальных элементов")
    println(" - Map<K, V>:  неизменяемый  словарь,  сопоставляющий  ключи  со  значениями")
    println(" - MutableMap<K, V>:  изменяемый  словарь,  сопоставляющий  ключи  со  значениями")
    println()

    println("### Другие типы:")
    println(" - Enum<T>:  перечисление  значений")
    println(" - Data Class:  класс  для  хранения  данных")
    println(" - Sealed Class:  класс  с  ограниченным  набором  подклассов")
    println(" - Function<T>:  тип  функции")
    println(" - Lambda:  анонимная  функция")
}
